package com.example.webapphr.model.beans;

public class Enemigo {
    private int enemigoId;
    private String nombre;
    private int edad;
    private int idGenero;
    private int experienciaEnemigo;
    private String objeto;
    private float probabilidad;
    private int claseEnemigoId;
    private String Genero;
    private String Clase;
    private int ataqueEnemigo;

    public int getEnemigoId() {
        return enemigoId;
    }

    public void setEnemigoId(int enemigoId) {
        this.enemigoId = enemigoId;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public int getIdGenero() {
        return idGenero;
    }

    public void setIdGenero(int idGenero) {
        this.idGenero = idGenero;
    }

    public int getExperienciaEnemigo() {
        return experienciaEnemigo;
    }

    public void setExperienciaEnemigo(int experienciaEnemigo) {
        this.experienciaEnemigo = experienciaEnemigo;
    }

    public String getObjeto() {
        return objeto;
    }

    public void setObjeto(String objeto) {
        this.objeto = objeto;
    }

    public float getProbabilidad() {
        return probabilidad;
    }

    public void setProbabilidad(float probabilidad) {
        this.probabilidad = probabilidad;
    }

    public int getClaseEnemigoId() {
        return claseEnemigoId;
    }

    public void setClaseEnemigoId(int claseEnemigoId) {
        this.claseEnemigoId = claseEnemigoId;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String genero) {
        Genero = genero;
    }

    public String getClase() {
        return Clase;
    }

    public void setClase(String clase) {
        Clase = clase;
    }

    public int getAtaqueEnemigo() {
        return ataqueEnemigo;
    }

    public void setAtaqueEnemigo(int ataqueEnemigo) {
        this.ataqueEnemigo = ataqueEnemigo;
    }
}
